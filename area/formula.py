#circle
def area_of_circle(radius):
    area=3.14*radius*radius
    return area
#triangle
def area_of_triangle(base,height):
    triangle=1/2*base*height
    return triangle
#square
def area_of_square(side):
    square=side*side
    return square
#trapezium
def area_of_trapezoid(h,a,b):
    #trapezoid=1/2*h*(a+b)
    #return trapezoid
    return (a+b)*h/2
